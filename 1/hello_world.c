#include<stdio.h>
//メイン関数
int main(void){
    printf("HelloWorld HelloWorld\n");
    return 0; 
}

//解説
//#include ->プログラムに必要なヘッダーファイルを読み込む（組み込む）
//stdio.h ->標準入出力（スタンダードI/O）関連の関数、マクロと型宣言が含まれているヘッダファイルのこと
//int ->整数を扱う際の型宣言
//main()～ ->メイン関数の宣言
//void ->「引数（=変数）なし」という意味
//printf ->標準出力の関数
//\nなど ->エスケープシーケンス（改行やタブなどを表す）
//return ->関数が何らかの値を返す時や、関数の処理を終わらせたいときなどに使う
